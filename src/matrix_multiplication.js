function dot(T_matrix, color_matrix) {
	let rslt_matrix = [];
	for (let i = 0; i < T_matrix.length; i++) {
		let sum = 0;
		for (let j = 0; j < T_matrix[i].length; j++) {
			sum += T_matrix[i][j] * color_matrix[j];
		}
		rslt_matrix.push(sum);
	}
	return rslt_matrix;
}

function MatrixStringtoArray(matrix) {
	let array_matrix = [];
	let rows = matrix.split(' ');
	rows.forEach(function(row) {
    	array_matrix.push(row.split(','));
  	});
	return array_matrix;
}